    private void btnGenerarTicketActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerarTicketActionPerformed
        // TODO add your handling code here:
        
        FileWriter archivo = null;
        PrintWriter pw;
        try{
            archivo = new FileWriter("Ticket.txt",true);
            pw = new PrintWriter(archivo);
            pw.println("Factura Maxident");
            pw.println(this.lblNum.getText()+" "+this.lblNumFactura.getText());
            pw.println(this.lblFECHA.getText()+" "+this.lblFecha.getText());
            pw.println(this.lblSECRE.getText()+" "+this.lblSecretaria.getText());
            pw.println(this.lblDOC.getText()+" "+this.lblDoctor.getText());
            pw.println(this.lblCLIENTE.getText()+" "+this.lblCliente.getText());
            pw.println(this.lblPROD.getText()+" "+this.lblProducto.getText());
            pw.println(this.lblCANT.getText()+" "+this.lblCantidad.getText());
            pw.println(this.lblPRECIO.getText()+" "+this.lblPrecio.getText());
            pw.println(this.lblTOTAL.getText()+" "+this.lblTotal.getText());
            pw.println("-------------------------------------------");
        }catch(IOException ex){
            
        }finally {
            try {
                archivo.close();
                JOptionPane.showMessageDialog(null, "Ticket generado");
            } catch (IOException ex) {
                Logger.getLogger(InternalFrame_Ticket.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnGenerarTicketActionPerformed


