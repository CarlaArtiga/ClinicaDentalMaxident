    public void CargarClientes(){
        Conexion c = new Conexion();
        c.EstableceConexion();
        ArrayList cli = new ArrayList();
        this.cmbCliente.removeAllItems();
        cli = c.GetListaClientesFact();
        Iterator iterador = cli.iterator();
        while(iterador.hasNext()){
            ComboClienteCita CCC = (ComboClienteCita) iterador.next();
            this.cmbCliente.addItem(CCC.getCliente());
        }
    }
    
    public void CargarDentista(){
        Conexion c = new Conexion();
        c.EstableceConexion();
        ArrayList d = new ArrayList();
        this.cmbDoctor.removeAllItems();
        d = c.GetListaDentistaFact();
        Iterator iterador = d.iterator();
        while(iterador.hasNext()){
            ComboDentistaFact CDF = (ComboDentistaFact) iterador.next();
            this.cmbDoctor.addItem(CDF.getDentista());
        }
    }
    
    public void CargarSecretario(){
        Conexion c = new Conexion();
        c.EstableceConexion();
        ArrayList sec = new ArrayList();
        this.cmbSecretaria.removeAllItems();
        sec = c.GetListaSecreFact();
        Iterator iterador = sec.iterator();
        while(iterador.hasNext()){
            ComboSecreFact CSF = (ComboSecreFact) iterador.next();
            this.cmbSecretaria.addItem(CSF.getSecretario());
        }
    }
    public void LlenarComboProducto(){
        Conexion c = new Conexion();
        c.EstableceConexion();
        ArrayList p = new ArrayList();
        this.cmbProducto.removeAllItems();
        p = c.GetListaProducto();
        Iterator iterador = p.iterator();
        while(iterador.hasNext()){
            ComboProducto CP = (ComboProducto) iterador.next();
            this.cmbProducto.addItem(CP.getProducto());
        }
    }

