    private void btnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarActionPerformed
        // TODO add your handling code here:
        Personas pe = new Personas();
        int cargos = this.cmbCargos.getSelectedIndex()+1;
        String Nom = this.txtNombre.getText();
        String Ape = this.txtApellido.getText();
        String FechaNac = ((JTextField)this.jdcFechaNac.getDateEditor().getUiComponent()).getText();
        String FechaReg = ((JTextField)this.jdcFechaRegistro.getDateEditor().getUiComponent()).getText();
        int DUI = parseInt(this.txtDUI.getText());
        int Tel = parseInt(this.txtTel.getText());
        String Dir = this.txtDir.getText();
                        
        try{
            if(this.rdbActivo.isSelected()){
               String Activo = this.rdbActivo.getText(); 
               Activo = "A";
                if(pe.InsertarPersonas(cargos, Nom, Ape, FechaNac, FechaReg, DUI, Tel, Dir, Activo)>0){
                    JOptionPane.showMessageDialog(null, "datos ingresados");
                    this.TablePersonas.setModel(personas.CargarPersona());
                }
            }
            
            if(this.rdbDesactivo.isSelected()){
                String Desactivo = this.rdbDesactivo.getText();
                Desactivo = "D";
                if(pe.InsertarPersonas(cargos, Nom, Ape, FechaNac, FechaReg, DUI, Tel, Dir, Desactivo)>0){
                    JOptionPane.showMessageDialog(null, "datos ingresados");  
                    this.TablePersonas.setModel(personas.CargarPersona());
                }
            }
            
            
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null, "error: " + ex);
        }
    }//GEN-LAST:event_btnInsertarActionPerformed

