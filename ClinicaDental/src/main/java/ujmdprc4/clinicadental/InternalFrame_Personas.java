    private void TablePersonasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablePersonasMouseClicked
        // TODO add your handling code here:
        int seleccion = this.TablePersonas.getSelectedRow(); 
        
        String Cargos = this.TablePersonas.getValueAt(seleccion, 0).toString();
        switch(Cargos){
            case "Dentista":
                this.cmbCargos.setSelectedItem("Dentista");
            break;
            case "Secretario":
                this.cmbCargos.setSelectedItem("Secretario");
            break;
            case "Maxilofacial":
                this.cmbCargos.setSelectedItem("Maxilofacial");
            break;
            case "Cliente":
                this.cmbCargos.setSelectedItem("Cliente");
            break;
                
        }
        this.txtNombre.setText(this.TablePersonas.getValueAt(seleccion, 1).toString());
        this.txtApellido.setText(this.TablePersonas.getValueAt(seleccion, 2).toString());
        ((JTextField)this.jdcFechaRegistro.getDateEditor().getUiComponent()).setText(this.TablePersonas.getValueAt(seleccion, 3).toString());
        ((JTextField)this.jdcFechaNac.getDateEditor().getUiComponent()).setText(this.TablePersonas.getValueAt(seleccion, 4).toString());
        this.txtDUI.setText(this.TablePersonas.getValueAt(seleccion, 5).toString());
        this.txtTel.setText(this.TablePersonas.getValueAt(seleccion, 6).toString());
        this.txtDir.setText(this.TablePersonas.getValueAt(seleccion, 7).toString());
        String estado = this.TablePersonas.getValueAt(seleccion, 8).toString();
        if(estado.equals("A")){
            this.rdbActivo.setSelected(true);
            this.rdbDesactivo.setSelected(false);
        }else{
            this.rdbDesactivo.setSelected(true);
            this.rdbActivo.setSelected(false);
        }
        
        
        
    }//GEN-LAST:event_TablePersonasMouseClicked


