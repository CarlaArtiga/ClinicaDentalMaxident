    public void LlenarCombo(){
        Conexion c = new Conexion();
        c.EstableceConexion();
        ArrayList cargos = new ArrayList();
        this.cmbCargos.removeAllItems();
        cargos = c.GetListaCargos();
        Iterator iterador = cargos.iterator();
        while(iterador.hasNext()){
            ComboCargos CG = (ComboCargos) iterador.next();
            this.cmbCargos.addItem(CG.getCargos());
        }
        
    }
    
    

